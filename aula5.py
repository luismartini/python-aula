# Operadores Aritméticos

# +
# -
# //
# **
# %
# ()

print('Soma: ', 2 + 2)
print('Concatenação:', '5' + '5')  #duas strings
print('Substração:', 2 - 2)
print(2 * 2)
print(10 * 'Luís')  #repetição
print(15 // 10)
print('Exponenciação: ', 2**3)
print('Módulo: ', 4 % 2)
print('Módulo: ', 5 % 2)
print('Luís' + ' ' + 'Martini e ele tem ' + str(44) + ' anos')

print('='*20)
print('//'*5 )

'''
Precedência:

( n + n ) - Os parênteses têm a maior precedência, contas dentro deles são realizadas primeiro

** - Depois vem a exponenciação

* / //  % - Na sequência multiplicação, divisão, divisão inteira e módulo

+  - - Por fim, soma e subtração
'''
print(5+2*10)